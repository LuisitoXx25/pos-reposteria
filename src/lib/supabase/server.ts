import { createServerClient } from "@supabase/ssr";
import { cookies } from "next/headers";

// Este cliente se usa en Server Components y Server Actions
// Necesita acceso a las cookies para leer/escribir la sesiÃ³n del usuario
export async function createClient() {
  const cookieStore = await cookies();

  return createServerClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!,
    {
      cookies: {
        getAll() {
          return cookieStore.getAll();
        },
        setAll(cookiesToSet) {
          try {
            cookiesToSet.forEach(({ name, value, options }) =>
              cookieStore.set(name, value, options)
            );
          } catch {
            // setAll se llama desde Server Components donde no se pueden
            // escribir cookies. Esto es esperado y se puede ignorar si
            // tienes el middleware configurado para refrescar las sesiones.
          }
        },
      },
    }
  );
}
